// Function to convert text to selected font
function createFontConverter(fontArray) {
  if (!fontArray || fontArray.length === 0) {
    return (text) => text; // Return original if no font
  }
  
  const normal = 'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
  return (text) => {
    let result = '';
    for (let char of text) {
      const index = normal.indexOf(char);
      if (index !== -1 && index < fontArray.length) {
        result += fontArray[index];
      } else {
        result += char;
      }
    }
    return result;
  };
}

